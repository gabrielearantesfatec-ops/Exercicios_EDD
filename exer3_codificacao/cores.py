cor = input("Digite uma cor: ")

if cor not in ("AZUL", "VERMELHO", "AMARELO"):
  print("Essa cor não é primária!")
  
elif cor == "AZUL":
  print("\nAzul: Calma, Inteligência, Frio")
    
elif cor == "VERMELHO":
  print("\nVermelho: calor, energia, perigo")
    
elif cor == "AMARELO":
  print("\nAmarelo: alegria, riqueza, luz")